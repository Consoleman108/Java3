package homework7.classForTest;

public class ClassForTest {

    public boolean not(boolean value) {
        return !value;
    }

    public boolean and(boolean value1, boolean value2) {
        return value1 && value2;
    }

    public boolean or(boolean value1, boolean value2) {
        return value1 || value2;
    }

    public boolean xor(boolean value1, boolean value2) {
        return value1 ^ value2;
    }

}
